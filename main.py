# Custome Calculator Functions

def add_two_values(value1, value2):
        x = value1
        y = value2
        return x + y


def subs_two_values(value1, value2):
        x = value1
        y = value2
        return x - y


def div_two_values(value1, value2):
        x = value1
        y = value2
        return x / y


